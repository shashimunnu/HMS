<?php
session_start();
include '../../config/db_connect.php';
include '../../includes/functions.php';

// Check if user is logged in
checkLogin();

$success_message = '';
$error_message = '';

if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    // Validate and sanitize input
    $name = filter_var($_POST['name'], FILTER_SANITIZE_STRING);
    $dob = $_POST['dob'];
    $gender = $_POST['gender'];
    $phone = filter_var($_POST['phone'], FILTER_SANITIZE_STRING);
    $email = filter_var($_POST['email'], FILTER_SANITIZE_EMAIL);
    $address = filter_var($_POST['address'], FILTER_SANITIZE_STRING);
    $blood_group = filter_var($_POST['blood_group'], FILTER_SANITIZE_STRING);

    // Basic validation
    if (empty($name) || empty($dob) || empty($gender)) {
        $error_message = "Name, Date of Birth, and Gender are required fields.";
    } else {
        // Insert into database
        $stmt = $conn->prepare("INSERT INTO patients (name, dob, gender, phone, email, address, blood_group) VALUES (?, ?, ?, ?, ?, ?, ?)");
        $stmt->bind_param("sssssss", $name, $dob, $gender, $phone, $email, $address, $blood_group);
        
        if ($stmt->execute()) {
            $success_message = "Patient added successfully!";
            // Clear form data after successful submission
            $_POST = array();
        } else {
            $error_message = "Error adding patient: " . $conn->error;
        }
        $stmt->close();
    }
}
